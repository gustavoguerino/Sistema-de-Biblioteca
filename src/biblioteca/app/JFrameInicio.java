    private void BotaoEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoEntrarActionPerformed
       // Chamado quando bot√£o de entrada for apertado
        if(CampoSenha.getText().isEmpty() || CampoUsuario.getText().isEmpty()){ //Caso um dos campos esteja vazio
            LabelAvisos.setText("Digite Usuario e Senha para entrar!");         //Exibi essa mensagem
            LabelAvisos.setVisible(true);           
        }
        else{
            if(biblio.logarUsuario(CampoUsuario.getText(), CampoSenha.getText())){
                dispose();             //Destruir o jframe
            }
            else{
                LabelAvisos.setText("Senha ou Usuario incorreto!"); //Caso logarUsuario retorne false exibir isso
                LabelAvisos.setVisible(true);
            }
        }
    }//GEN-LAST:event_BotaoEntrarActionPerformed

